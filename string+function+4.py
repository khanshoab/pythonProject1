# lstrip() - This method is used to remove the space which are at left side of the string.
num = "     everyone"
str1 = num.lstrip()
print(num)
print(str1)
# rstrip() - This method is used to remove the space which are at right side of the string.
num = "everyone    "
str1 = num.rstrip()
print(num)
print(str1)
# strip() - This method is used to remove the space from the both side of the strong.
num = "     everyone     "
str1 = num.strip()
print(num)
print(str1)